class Var
{
    public static void main(String[] args) 
    {
        // ! Variables Are Like Containers...
        int a = 5;

        //* int -> Data Type
        //* a -> Variable Name
        //* 5 -> Initial Value(Data)

        System.out.println(a);

        //! Data Types In Java:
        // int
        // float
        // double
        // char
        // boolean
        // String
        // byte
        // short
        // long
        // void

        //! Variable Naming Conventions:

        // 1. Variable Name can not contain any Special characters
        // 2. Variable Can Not contain any number in start
        // 3. Variable is Case Sensitive

        int A = 5;
        int MyAge = 45;
        int MyAge1 = 45;

        // Camelcase Convention
        int myAgeIsLess = 45;

        // Pascalcase Convention
        int MyAgeIsLess = 45;

        // Kebabcase Convention (Not Supported In Java)
        // int my-age-is-less2 = 45;

        // Snakecase Convention
        int my_age_is_less3 = 45;

    }
}